from sanio.base import BaseSanio


class NumericAggregator(BaseSanio):
    @classmethod
    def average(cls, series):
        return None

    @classmethod
    def max(cls, series):
        return None

    @classmethod
    def median(cls, series):
        return None

    @classmethod
    def min(cls, series):
        return None

    @classmethod
    def percent_of_average(cls, value, series):
        return None
